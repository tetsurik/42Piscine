int		find_empty(char grid[9][9])
{
	int i;
	int j;
	int empty[2];

	i = 0;
	j = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (&grid[i][j] == 0)
			{
				empty[0] = i;
				empty[1] = j;
				return(empty);
			}
			j++;
		}
		i++;
	}
	return (0);
}
