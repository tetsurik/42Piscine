char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	temp;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (i != 0)
	{
		temp[j] = str[i];
		--i;
		++j;
	}
	return (temp);
}
