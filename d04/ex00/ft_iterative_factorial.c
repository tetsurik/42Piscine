int		ft_iterative_factorial(int nb)
{
	int i;
	int j;

	if (nb == 0)
		return (1);
	else if (nb < 0 || nb >= 13)
		return (0);
	j = 1;
	i = 0;
	while (i++ < nb)
		j *= i;
	return (j);
}
