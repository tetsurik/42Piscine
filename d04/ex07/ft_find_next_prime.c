int	ft_find_next_prime(int nb)
{
	int j;
	int i;

	j = 0;
	while (j = 0)
	{
		i = nb / 2;
		if (nb <= 1)
			j = 0;
		else
			while (i > 1)
				if (nb % i == 0)
					j = (0);
				else
					--i;
		j = 1;
		if (j = 1)
			return (nb);
		else
			++nb;
	}
}
